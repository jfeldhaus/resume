-- This query displays all versions for reported bugs
-- and the number of bugs associated with each version. 
SELECT * FROM (
  SELECT 

	CASE WHEN GROUPING (RPTHEAD.UTILITY_VERSION) = 0
	THEN RPTHEAD.UTILITY_VERSION || ' (' ||
	  COUNT (RPTHEAD.UTILITY_VERSION) || ')'
	ELSE '<All> (' || COUNT (*) || ')'
	END AS "_UVIEW1", -- VERSION --
	
	CASE WHEN GROUPING (RPTHEAD.UTILITY_VERSION) = 0
	THEN RPTHEAD.UTILITY_VERSION ELSE '%'
	END AS "_UVIEW2" -- VALUE --
	
  FROM BUG.RPTHEAD
  GROUP BY ROLLUP (RPTHEAD.UTILITY_VERSION)

  UNION

  SELECT 
    '<' || SUBSTR (UTILITY_VERSION, 1, INSTR (UTILITY_VERSION, '.', 1, 3) - 1) || 
      '%> (' || COUNT (*) || ')' AS "_UVIEW1", -- VERSION --
      
    SUBSTR (UTILITY_VERSION, 1, INSTR (UTILITY_VERSION, '.', 1, 3) - 1) || 
      '%' AS "_UVIEW2" -- VALUE --
  
  FROM BUG.RPTHEAD
  
  GROUP BY (SUBSTR (UTILITY_VERSION, 1, INSTR (UTILITY_VERSION, '.', 1, 3) - 1))
  HAVING COUNT (*) >= 100 AND 
    SUBSTR (UTILITY_VERSION, 1, INSTR (UTILITY_VERSION, '.', 1, 3) - 1)  IS NOT NULL

  UNION

  SELECT 
    '<' || SUBSTR (UTILITY_VERSION, 1, INSTR (UTILITY_VERSION, '.', 1, 2) - 1) || 
      '%> (' || COUNT (*) || ')' AS "_UVIEW1", -- VERSION --
      
    SUBSTR (UTILITY_VERSION, 1, INSTR (UTILITY_VERSION, '.', 1, 2) - 1) || 
      '%' AS "_UVIEW2" -- VALUE --
  
  FROM BUG.RPTHEAD
  
  GROUP BY (SUBSTR (UTILITY_VERSION, 1, INSTR (UTILITY_VERSION, '.', 1, 2) - 1))
  HAVING COUNT (*) >= 1000 AND 
    SUBSTR (UTILITY_VERSION, 1, INSTR (UTILITY_VERSION, '.', 1, 2) - 1)  IS NOT NULL)

ORDER BY "_UVIEW1" DESC NULLS FIRST;



