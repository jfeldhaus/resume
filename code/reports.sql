-- Cumulative code bug statistics over year intervals
SELECT 

  EXTRACT (YEAR FROM RPTDATE) AS "YEAR",
  
  COUNT (*) AS "FILED BUG COUNT",
  COUNT (FIXED_DATE) AS "FIXED BUG COUNT",
  COUNT (CLOSED_DATE) AS "CLOSED BUG COUNT",
  COUNT (*) - COUNT (FIXED_DATE) AS "OPEN BUG COUNT",

  SUM (COUNT (*)) OVER (ORDER BY EXTRACT (YEAR FROM RPTDATE)
    ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS "CUM. FILED BUG COUNT",
    
  SUM (COUNT (FIXED_DATE)) OVER (ORDER BY EXTRACT (YEAR FROM RPTDATE)
    ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS "CUM. FIXED BUG COUNT",
  
  SUM (COUNT (CLOSED_DATE)) OVER (ORDER BY EXTRACT (YEAR FROM RPTDATE)
    ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS "CUM. CLOSED BUG COUNT",    
  
  SUM (COUNT (*) - COUNT (FIXED_DATE)) OVER (ORDER BY EXTRACT (YEAR FROM RPTDATE)
    ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS "CUM. OPEN BUG COUNT"

FROM BUG.RPTHEAD
WHERE RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS)

GROUP BY (EXTRACT (YEAR FROM RPTDATE))
ORDER BY (EXTRACT (YEAR FROM RPTDATE)) DESC
;


-- Code bugs broken down by submitter and bug state 
SELECT 

  CASE WHEN GROUPING (RPTD_BY) > 0 
    THEN '** TOTAL **' ELSE RPTD_BY 
    END AS "REPORTED BY", 
  
  COUNT (*) AS "TOTAL BUG COUNT", 
  COUNT (*) - COUNT (FIXED_DATE) AS "OPEN BUG COUNT",
  COUNT (FIXED_DATE) AS "CLOSED BUG COUNT",
  
  ROUND (AVG (CASE WHEN FIXED_DATE IS NULL 
    THEN SYSDATE 
    ELSE FIXED_DATE 
    END - RPTDATE)) AS "AVG DAYS OPEN",
  
  ROUND (AVG (CS_PRIORITY), 1) AS "AVG FILER SEVERITY",
  ROUND (AVG (PRI_FOR_DEV), 1) AS "AVG DEV PRIORITY"

FROM BUG.RPTHEAD

WHERE RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS)

GROUP BY ROLLUP (RPTD_BY)
ORDER BY COUNT (*) DESC;




-- All open code bugs broken down by submitter, bug count, and percent of total
SELECT 

  CASE WHEN RPTD_BY IS NULL 
    THEN '** TOTAL **' 
    ELSE RPTD_BY END AS "REPORTED BY", 
  
  COUNT (*) AS "OPEN BUG COUNT",
  
  TO_CHAR (ROUND (COUNT (RPTD_BY) / (SUM (CAST (COUNT (RPTD_BY) AS NUMBER)) 
    OVER (PARTITION BY GROUPING (RPTD_BY))), 2) * 100) || '%' AS PERCENTAGE

FROM BUG.RPTHEAD
WHERE FIXED_DATE IS NULL
  AND RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS)
  
GROUP BY ROLLUP (RPTD_BY)
ORDER BY COUNT (RPTD_BY) / (SUM (CAST (COUNT (RPTD_BY) AS NUMBER)) OVER ()) DESC, 1;



-- All open code bugs broken down by severity, bug count, and percent of total
SELECT 

  CASE WHEN GROUPING_ID (CS_PRIORITY) > 0 
    THEN '** TOTAL **' 
    ELSE TO_CHAR (CS_PRIORITY) END AS "SEVERITY",
  
  COUNT (CS_PRIORITY) AS "OPEN BUG COUNT",
  
  TO_CHAR (ROUND (COUNT (CS_PRIORITY) / (SUM (CAST (COUNT (CS_PRIORITY) AS NUMBER)) 
    OVER (PARTITION BY GROUPING_ID (CS_PRIORITY))), 2) * 100) || '%' AS PERCENTAGE

FROM BUG.RPTHEAD
WHERE FIXED_DATE IS NULL 
  AND RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS)
  
