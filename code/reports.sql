-- This query displays customers associated with reported bugs
-- and the number of bugs associated with each customer.
SELECT CUSTOMER, VALUE  FROM (

  SELECT
    
    CASE       
      WHEN GROUPING (ORG) = 1
      THEN '<External> (' || COUNT (ORG) || ')'
      WHEN GROUPING (ORG) = 0 AND ORG IS NOT NULL
      THEN ORG || ' (' || COUNT (ORG) || ')'
      ELSE '<Internal> (' || COUNT (*) || ')'
      END AS "CUSTOMER",
    
    CASE 
      WHEN GROUPING (ORG) = 1
      THEN '%'
      WHEN GROUPING (ORG) = 0 AND ORG IS NOT NULL
      THEN ORG 
      ELSE '<Internal>'
      END AS "VALUE",
      
    CASE       
      WHEN GROUPING (ORG) = 1
      THEN COUNT (ORG) 
   	  WHEN ORG IS NOT NULL
   	  THEN NULL 
   	  ELSE COUNT (*) END AS "GROUP_COUNT"   
    
  FROM RPTHEAD 
  LEFT OUTER JOIN (
    /* select a maximum of one customer per bug report */
    SELECT DISTINCT 
      RPTNO, 
      
      -- This expression takes 13 secs. in TimesTen.
      -- Avoiding this adds extra rows to the result
      -- and an incorrect RPTNO count for <External>.
      FIRST_VALUE (ORG) 
        OVER (PARTITION BY RPTNO ORDER BY ORG ASC) AS ORG 
        
    FROM CUSTOMER_BUGS
    WHERE ORG IS NOT NULL) CUST_BUGS
    ON RPTHEAD.RPTNO = CUST_BUGS.RPTNO
  
  GROUP BY ROLLUP (ORG)
  
  UNION
  
  SELECT 
  	'<All> (' || COUNT (*) || ')' AS "CUSTOMER",
  	'<All>' AS "VALUE",
  	COUNT (*) AS "GROUP_COUNT"
  FROM RPTHEAD)

ORDER BY GROUP_COUNT DESC NULLS LAST, CUSTOMER;




-- Displays all priorites for reported bugs and the number 
-- of bugs associated with a priority. 
SELECT

	CASE WHEN GROUPING (RPTHEAD.PRI_FOR_DEV) = 0 
	THEN 
	
	  CASE 
	    WHEN RPTHEAD.PRI_FOR_DEV IS NULL
	      THEN '<Unassigned>'
	    WHEN RPTHEAD.PRI_FOR_DEV = 1
	      THEN RPTHEAD.PRI_FOR_DEV || 
	        ' - At Earliest Opportunity'
	    WHEN RPTHEAD.PRI_FOR_DEV = 2
	      THEN RPTHEAD.PRI_FOR_DEV || 
	        ' - By Next Distributed Version'
	    WHEN RPTHEAD.PRI_FOR_DEV = 3
	      THEN RPTHEAD.PRI_FOR_DEV || 
	        ' - By Next Release'
	    WHEN RPTHEAD.PRI_FOR_DEV = 4
	      THEN RPTHEAD.PRI_FOR_DEV || 
	        ' - Scheduled for a Later Release'
	    WHEN RPTHEAD.PRI_FOR_DEV = 5
	      THEN RPTHEAD.PRI_FOR_DEV || 
	        ' - Not Scheduled'	        
	  END
	
	  || ' (' || COUNT (*) || ')'
	  
	ELSE '<All> (' || COUNT (*) || ')'
    END AS "PRIORITY",
	
    CASE WHEN GROUPING (RPTHEAD.PRI_FOR_DEV) = 1
      THEN '%'  
      WHEN RPTHEAD.PRI_FOR_DEV IS NULL
      THEN '<Unassigned>'
      ELSE RPTHEAD.PRI_FOR_DEV
      END AS "VALUE"

FROM BUG.RPTHEAD

GROUP BY ROLLUP (RPTHEAD.PRI_FOR_DEV)

ORDER BY RPTHEAD.PRI_FOR_DEV ASC NULLS FIRST;




-- This query displays all severities for reported bugs
-- and the number of bugs associated with a severity. 
SELECT

	CASE WHEN GROUPING (RPTHEAD.CS_PRIORITY) = 0 
	THEN RPTHEAD.CS_PRIORITY || ' - ' || 
	
	  CASE 
	    WHEN RPTHEAD.CS_PRIORITY = 1
	      THEN 'Complete Loss of Service'
	    WHEN RPTHEAD.CS_PRIORITY = 2
	      THEN 'Severe Loss of Service'
	    WHEN RPTHEAD.CS_PRIORITY = 3
	      THEN 'Minimal Loss of Service'
	    WHEN RPTHEAD.CS_PRIORITY = 4
	      THEN 'Minor Error, No Loss of Service'
	  END
	
	  || ' (' || COUNT (RPTHEAD.CS_PRIORITY) || ')'
	  
	ELSE '<All> (' || COUNT (*) || ')'
    END AS "SEVERITY",
	
    CASE WHEN GROUPING (RPTHEAD.CS_PRIORITY) = 0  
      THEN TO_CHAR (RPTHEAD.CS_PRIORITY) 
      ELSE '%' END AS "VALUE"

FROM BUG.RPTHEAD
GROUP BY ROLLUP (RPTHEAD.CS_PRIORITY)

ORDER BY RPTHEAD.CS_PRIORITY ASC NULLS FIRST;


-- This query displays all states for reported bugs
-- and the number of bugs associated with a state. 
SELECT 
  STATUS AS "_UVIEW1", -- STATUS --
  "VALUE" AS "_UVIEW2" -- VALUE --
FROM (

  SELECT

	BUGSTAT.STATUS || ' - ' || SDESCRIP ||
      ' (' || COUNT (RPTNO) || ')'  AS "STATUS",		
		
	TO_CHAR (BUGSTAT.STATUS) AS "VALUE",
      
    COUNT (RPTNO) AS "DISPLAY_ORDER",
    
	0 AS "STATE_VALUE"

  FROM BUG.BUGSTAT, BUG.RPTHEAD
  WHERE BUGSTAT.STATUS = RPTHEAD.STATUS
  GROUP BY (BUGSTAT.STATUS, SDESCRIP)

  UNION

  SELECT
  
    CASE WHEN GROUPING (BUGSTAT_STATE.STATE) = 0 
