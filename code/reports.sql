-- Query for R analysis of report trends
SELECT 

  ALL_RPTS.MON,  
  
  -- ALL REPORTS --
  ALL_RPTS.RPT_COUNT,
  ALL_VERSIONS.PRODUCT_VERSION,

  CASE WHEN INTERNAL_DEFECTS.INTERNAL_DEFECT_COUNT IS NULL 
    THEN 0 
    ELSE INTERNAL_DEFECTS.INTERNAL_DEFECT_COUNT 
  END  + 
  
    CASE WHEN EXTERNAL_DEFECTS.EXTERNAL_DEFECT_COUNT IS NULL 
      THEN 0 
      ELSE EXTERNAL_DEFECTS.EXTERNAL_DEFECT_COUNT 
    END AS DEFECT_COUNT,


  CASE WHEN INTERNAL_DEFECTS_FIXED.INTERNAL_DEFECT_FIXED_COUNT IS NULL 
    THEN 0 
    ELSE INTERNAL_DEFECTS_FIXED.INTERNAL_DEFECT_FIXED_COUNT 
  END + 
    
    CASE WHEN EXTERNAL_DEFECTS_FIXED.EXTERNAL_DEFECT_FIXED_COUNT IS NULL 
      THEN 0 
      ELSE EXTERNAL_DEFECTS_FIXED.EXTERNAL_DEFECT_FIXED_COUNT 
    END AS DEFECT_FIXED_COUNT,
  
    
  -- INTERNAL REPORTS --    
  CASE WHEN INTERNAL_RPT_COUNT IS NULL 
    THEN 0 
    ELSE INTERNAL_RPT_COUNT 
  END AS INTERNAL_RPT_COUNT, 
    
  CASE WHEN INTERNAL_DEFECTS.INTERNAL_DEFECT_COUNT IS NULL 
    THEN 0 
    ELSE INTERNAL_DEFECTS.INTERNAL_DEFECT_COUNT 
  END AS INTERNAL_DEFECT_COUNT,    
    
  CASE WHEN INTERNAL_DEFECTS_FIXED.INTERNAL_DEFECT_FIXED_COUNT IS NULL 
    THEN 0 
    ELSE INTERNAL_DEFECTS_FIXED.INTERNAL_DEFECT_FIXED_COUNT 
  END AS INTERNAL_DEFECT_FIXED_COUNT,
          


  -- EXTERNAL REPORTS --   
  CASE WHEN EXTERNAL_RPT_COUNT IS NULL 
    THEN 0 
    ELSE EXTERNAL_RPT_COUNT 
  END AS EXTERNAL_RPT_COUNT, 

    
  CASE WHEN EXTERNAL_DEFECTS.EXTERNAL_DEFECT_COUNT IS NULL 
    THEN 0 
    ELSE EXTERNAL_DEFECTS.EXTERNAL_DEFECT_COUNT 
  END AS EXTERNAL_DEFECT_COUNT,    
    
  CASE WHEN EXTERNAL_DEFECTS_FIXED.EXTERNAL_DEFECT_FIXED_COUNT IS NULL 
    THEN 0 
    ELSE EXTERNAL_DEFECTS_FIXED.EXTERNAL_DEFECT_FIXED_COUNT 
  END AS EXTERNAL_DEFECT_FIXED_COUNT
  
  
    
FROM 

  (SELECT 
    
      TRUNC(RPTDATE, 'MON') AS MON,
      COUNT (*) AS RPT_COUNT
      
   FROM BUG.RPTHEAD 
   WHERE CATEGORY != 'IMPORT' AND SOURCE_SYSTEM IS NULL
   GROUP BY TRUNC(RPTDATE, 'MON')) ALL_RPTS,
   
  (SELECT DISTINCT

    TRUNC(RPTDATE, 'MON') AS MON,
    FIRST_VALUE (UTILITY_VERSION) 
      OVER (PARTITION BY TRUNC(RPTDATE, 'MON')
        ORDER BY COUNT (*) DESC, MAX (UTILITY_VERSION) DESC) PRODUCT_VERSION

   FROM BUG.RPTHEAD
   WHERE CATEGORY != 'IMPORT' AND SOURCE_SYSTEM IS NULL
   GROUP BY TRUNC(RPTDATE, 'MON'), UTILITY_VERSION) ALL_VERSIONS,


  
   -- INTERNAL REPORTS --   
  (SELECT 
  
    TRUNC(RPTDATE, 'MON') AS MON,
    COUNT (*) AS INTERNAL_RPT_COUNT
    
   FROM BUG.RPTHEAD
   WHERE RPTNO NOT IN (SELECT RPTNO FROM BUG.CUSTOMER_BUGS)
    AND CATEGORY != 'IMPORT' AND SOURCE_SYSTEM IS NULL
   GROUP BY TRUNC(RPTDATE, 'MON')) INTERNAL_RPTS,
   
  (SELECT 
  
    TRUNC(RPTDATE, 'MON') AS MON,
    COUNT (*) AS INTERNAL_DEFECT_COUNT
    
   FROM BUG.RPTHEAD
   WHERE RPTNO NOT IN (SELECT RPTNO FROM BUG.CUSTOMER_BUGS)
     AND RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS)
   GROUP BY TRUNC(RPTDATE, 'MON')) INTERNAL_DEFECTS,
   
  (SELECT 
  
    TRUNC(FIXED_DATE, 'MON') AS MON,
    COUNT (*) AS INTERNAL_DEFECT_FIXED_COUNT
    
   FROM BUG.RPTHEAD
   WHERE RPTNO NOT IN (SELECT RPTNO FROM BUG.CUSTOMER_BUGS)
     AND RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS)
     AND FIXED_DATE IS NOT NULL
   GROUP BY TRUNC(FIXED_DATE, 'MON')) INTERNAL_DEFECTS_FIXED,
   
   
   
  -- EXTERNAL REPORTS --   
  (SELECT 
  
    TRUNC(RPTDATE, 'MON') AS MON,
    COUNT (*) AS EXTERNAL_RPT_COUNT
    
   FROM BUG.RPTHEAD
   WHERE RPTNO IN (SELECT RPTNO FROM BUG.CUSTOMER_BUGS)
    AND CATEGORY != 'IMPORT' AND SOURCE_SYSTEM IS NULL
   GROUP BY TRUNC(RPTDATE, 'MON')) EXTERNAL_RPTS,
   
  (SELECT 
  
    TRUNC(RPTDATE, 'MON') AS MON,
    COUNT (*) AS EXTERNAL_DEFECT_COUNT
    
   FROM BUG.RPTHEAD
   WHERE RPTNO IN (SELECT RPTNO FROM BUG.CUSTOMER_BUGS)
     AND RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS)
   GROUP BY TRUNC(RPTDATE, 'MON')) EXTERNAL_DEFECTS,
  
  (SELECT 
  
    TRUNC(FIXED_DATE, 'MON') AS MON,
    COUNT (*) AS EXTERNAL_DEFECT_FIXED_COUNT
    
   FROM BUG.RPTHEAD
   WHERE RPTNO IN (SELECT RPTNO FROM BUG.CUSTOMER_BUGS)
     AND RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS)
     AND FIXED_DATE IS NOT NULL
   GROUP BY TRUNC(FIXED_DATE, 'MON')) EXTERNAL_DEFECTS_FIXED  
  
  
WHERE (
  ALL_RPTS.MON = ALL_VERSIONS.MON(+) AND
  
  ALL_RPTS.MON = INTERNAL_RPTS.MON(+) AND 
  ALL_RPTS.MON = INTERNAL_DEFECTS.MON(+) AND 
  ALL_RPTS.MON = INTERNAL_DEFECTS_FIXED.MON(+) AND
  
  ALL_RPTS.MON = EXTERNAL_RPTS.MON(+) AND 
  ALL_RPTS.MON = EXTERNAL_DEFECTS.MON(+) AND
  ALL_RPTS.MON = EXTERNAL_DEFECTS_FIXED.MON(+))

ORDER BY 1 DESC;


