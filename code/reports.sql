-- A query that reports general code bug count statistics broken down by submitter
-- and product component
WITH RPTHEAD_STATS AS (
 
  SELECT
  
    CASE WHEN GROUPING (RPTD_BY) > 0 
      THEN '** TOTAL **' ELSE RPTD_BY END AS "USER",
    
    CASE WHEN GROUPING (CATEGORY) > 0 
      THEN '** TOTAL **' ELSE CATEGORY END AS "COMPONENT",
   
    COUNT (RPTNO) AS "BUG COUNT",
    DENSE_RANK () OVER (PARTITION BY CATEGORY
      ORDER BY COUNT (RPTNO) DESC) AS "BUG COUNT RANK",
    
    COUNT (FIXED_DATE) AS "FIXED BUG COUNT",
    DENSE_RANK () OVER (PARTITION BY CATEGORY 
      ORDER BY COUNT (FIXED_DATE) DESC) AS "FIXED BUG COUNT RANK",
  
    COUNT (RPTNO) - COUNT (FIXED_DATE) AS "OPEN BUG COUNT",
    DENSE_RANK () OVER (PARTITION BY CATEGORY 
      ORDER BY COUNT (RPTNO) - COUNT (FIXED_DATE) DESC) AS "OPEN BUG COUNT RANK",
   
    ROUND (AVG (CASE WHEN FIXED_DATE IS NULL 
                THEN SYSDATE 
                ELSE FIXED_DATE END - RPTDATE)) AS "AVG DAYS OPEN",
    DENSE_RANK () OVER (PARTITION BY CATEGORY 
      ORDER BY AVG (CASE WHEN FIXED_DATE IS NULL 
                    THEN SYSDATE 
                    ELSE FIXED_DATE END - RPTDATE) ASC) AS "AVG DAYS OPEN RANK",
   
    ROUND (AVG (CS_PRIORITY), 1) AS "AVG FILER SEVERITY",
    DENSE_RANK () OVER (PARTITION BY CATEGORY 
      ORDER BY AVG (CS_PRIORITY) ASC) AS "AVG FILER SEVERITY RANK",
   
    ROUND (AVG (PRI_FOR_DEV), 1) AS "AVG DEV PRIORITY",
    DENSE_RANK () OVER (PARTITION BY CATEGORY 
      ORDER BY AVG (PRI_FOR_DEV) ASC) AS "AVG DEV PRIORITY RANK"
    
  FROM RPTHEAD 
  
  -- exclude non product bugs
  WHERE RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS) 
      
  GROUP BY CUBE (RPTD_BY, CATEGORY)
  HAVING COUNT (RPTNO) > 100
)

SELECT * FROM RPTHEAD_STATS
ORDER BY "COMPONENT", "BUG COUNT RANK" ASC, "AVG DAYS OPEN RANK" ASC ;



