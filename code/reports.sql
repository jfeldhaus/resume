  CASE WHEN GROUPING (TO_CHAR (RPTCALENDAR.RPTDATE, 'DY')) > 0 
    THEN '** TOTAL **' 
    ELSE TO_CHAR (RPTCALENDAR.RPTDATE, 'DY') END AS "DAY OF WEEK",

  COUNT (RPTNO) AS "BUG COUNT"

FROM BUG.RPTHEAD
RIGHT OUTER JOIN BUG.RPTCALENDAR 
 ON TRUNC (RPTHEAD.RPTDATE) = RPTCALENDAR.RPTDATE

GROUP BY ROLLUP (TO_CHAR (RPTCALENDAR.RPTDATE, 'DY'))
ORDER BY 2 DESC;



-- A query that returns a sliding window average bug count 
SELECT 

  TO_CHAR (RPTCALENDAR.RPTDATE, 'YYYY-MON-DD (DY)') AS "REPORTED DATE",
  COUNT (RPTNO) AS "NEW BUG COUNT",
  
  ROUND (AVG (COUNT (RPTNO))
    OVER (ORDER BY RPTCALENDAR.RPTDATE ASC 
      ROWS BETWEEN 14 PRECEDING AND CURRENT ROW), 1) AS "AVG PER DAY (LAST 14 DAYS)"

FROM BUG.RPTHEAD
RIGHT OUTER JOIN BUG.RPTCALENDAR 
 ON TRUNC (RPTHEAD.RPTDATE) = RPTCALENDAR.RPTDATE 

GROUP BY RPTCALENDAR.RPTDATE
ORDER BY RPTCALENDAR.RPTDATE DESC;



-- A list of all assertion failure bugs
WITH ASSERTION_MSSGS AS (
  
  SELECT 
    RPTNO, 
    LINENO, 
    COMMENTS AS "COMMENT"
  FROM BUG.RPTBODY 
  WHERE UPPER (COMMENTS) LIKE '%ASSERTION FAILED:%'
),

ASSERTION_BUGS AS (

  SELECT 
    RPTHEAD.RPTNO, 
    UTILITY_VERSION AS "VERSION", 
    SUBJECT, 
    LINENO, 
    "COMMENT"
  FROM BUG.RPTHEAD 
  INNER JOIN (SELECT * FROM ASSERTION_MSSGS) ASSERTION_MSSGS
    ON RPTHEAD.RPTNO = ASSERTION_MSSGS.RPTNO
)

SELECT * FROM (

  SELECT DISTINCT 
    RPTNO, 
    VERSION, 
    SUBJECT,   
    FIRST_VALUE ("COMMENT") 
      OVER (PARTITION BY RPTNO ORDER BY LINENO ASC) AS "FIRST COMMENT"  
  FROM ASSERTION_BUGS) ASSERTION_BUG_SUMMARY 

ORDER BY RPTNO DESC
;


