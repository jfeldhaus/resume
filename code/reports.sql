  SELECT 
    ROWID AS TIME_ID,
    RPTDATE AS RPTDATE,
    TO_CHAR (RPTDATE, 'DD') AS DAY_OF_MONTH,
    TO_CHAR (RPTDATE, 'DY') AS DAY_OF_WEEK,
