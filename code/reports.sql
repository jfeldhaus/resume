ORDER BY COUNT (CS_PRIORITY) / 
  (SUM (CAST (COUNT (CS_PRIORITY) AS NUMBER)) 
    OVER (PARTITION BY GROUPING_ID (CS_PRIORITY))) DESC;


-- All open code bugs broken down by dev. priority, bug count, and percent of total
