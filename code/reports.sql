                  FROM RPTHEAD, CATEGORY_DIM
                  WHERE RPTHEAD.CATEGORY = CATEGORY_DIM.CATEGORY) OPEN_SET
  
  WHERE OPEN_DATE = TIME_DIM.RPTDATE
  
  UNION ALL
  
  SELECT 
    
