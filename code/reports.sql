  CASE WHEN GROUPING (PRI_FOR_DEV) > 0 
    THEN '** TOTAL **'
    WHEN PRI_FOR_DEV IS NULL 
    THEN 'UNASSIGNED' 
    ELSE PRI_FOR_DEV END AS "PRIORITY", 

  COUNT (*) AS "OPEN BUG COUNT",
  
  TO_CHAR (ROUND (COUNT (*) / (SUM (CAST (COUNT (*) AS NUMBER)) 
    OVER (PARTITION BY GROUPING (PRI_FOR_DEV))), 2) * 100) || '%' AS PERCENTAGE

FROM BUG.RPTHEAD

WHERE FIXED_DATE IS NULL  
  AND RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS)
  
GROUP BY ROLLUP (PRI_FOR_DEV)

ORDER BY COUNT (*) / (SUM (CAST (COUNT (*) AS NUMBER)) 
  OVER (PARTITION BY GROUPING (PRI_FOR_DEV))) DESC;


-- All code bugs broken down by status, bug count, and percent of total count
SELECT 
  CASE WHEN GROUPING (HEAD.STATUS) > 0 
    THEN '** TOTAL **' 
    ELSE TO_CHAR (HEAD.STATUS) END AS "STATUS DESCRIPTION",
    
  BUGSTAT.SDESCRIP AS "DESCRIPTION", 
  
  COUNT (*) AS "BUG COUNT",
  
  TO_CHAR (ROUND (COUNT (*) / (SUM (CAST (COUNT (*) AS NUMBER)) 
    OVER (PARTITION BY GROUPING (HEAD.STATUS))), 2) * 100) || '%' AS PERCENTAGE

FROM BUG.RPTHEAD HEAD, BUG.BUGSTAT

WHERE HEAD.STATUS = BUGSTAT.STATUS
  AND RPTNO IN (SELECT * FROM BUG.V_CODE_BUGS)

GROUP BY BUGSTAT.SDESCRIP, CUBE (HEAD.STATUS)
HAVING GROUPING (HEAD.STATUS) > 0

ORDER BY "BUG COUNT" DESC, 1 NULLS FIRST, 2 NULLS FIRST ;


