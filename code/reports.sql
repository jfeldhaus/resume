   UPPER (COMMENTS) LIKE '%ASSERT%'
),

ASSERTION_BUGS AS (

  SELECT 
    RPTHEAD.RPTNO, 
    CATEGORY,
    RPTD_BY
  FROM BUG.RPTHEAD 
  INNER JOIN (SELECT RPTNO FROM ASSERTION_MSSGS) ASSERTION_MSSGS
    ON RPTHEAD.RPTNO = ASSERTION_MSSGS.RPTNO
)


